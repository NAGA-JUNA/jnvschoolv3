// Determine active page by URL match
function _navIsActive($url, $currentPage) {
    $map = [
        '/' => 'home',
        '/public/about.php' => 'about',
        '/public/teachers.php' => 'teachers',
        '/public/notifications.php' => 'notifications',
        '/public/gallery.php' => 'gallery',
